#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>

/**
 * op_add - addition
 * @a: integer one
 * @b: integer two
 * Return: 0
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction
 * Return: 0
 */

int op_sub(int a, int b);
{
	return (a - b);
}

/**
 * op_mul - multiplication
 * Return: 0
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Division
 * Return: 0
 */

int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Modulos
 * Return: 0
 */
int op_mod(int a, int b)
{
	return (a % b);
}

